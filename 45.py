def suma():
	D = sum([9, 4, 5])
	return D 


def lena():
	D = len([9, 4, 5])
	return D 


def maxi():
	D = max([9, 4, 5])
	return D 


def mini():
	D = min([9, 4, 5])
	return D 


def sorti(a = [3, 2, 5 ,4, 7, 1]):
	D = sorted(a)
	return D 


print(suma())
print(lena())
print(maxi())
print(mini())
print(sorti())
